    public void guardarEstadoPuerta(String estado) {
        Connection cnn = conexion.establecerConexion();
        if (cnn != null) {
            try {
                String sql = "INSERT INTO EstadoObjeto (fecha, estado) VALUES (?, ?)";
                PreparedStatement ps = cnn.prepareStatement(sql);

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                String fecha = sdf.format(new Date());

                ps.setString(1, fecha);
                ps.setString(2, estado);

                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Estado de la puerta guardado con Ã©xito.");
                    lblFechaHora.setText("Hora: "+ fecha);
                } else {
                    JOptionPane.showMessageDialog(this, "Error al guardar el estado de la puerta.", "Error", JOptionPane.ERROR_MESSAGE);
                }

                ps.close();
                cnn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al guardar el estado de la puerta.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo conectar a la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    



